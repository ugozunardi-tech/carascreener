export type NodeType  = 'company' | 'theme';
export type EdgeType  = 'supplier' | 'customer' | 'foundry' | 'material' | 'technology' | 'power' | 'testing' | 'thematic';
export type RiskLevel = 'critical' | 'high' | 'medium' | 'low';
export type NodeRole  = 'upstream' | 'midstream' | 'downstream' | 'hyperscaler' | 'infrastructure';
export type Region    = 'US' | 'TW' | 'KR' | 'NL' | 'JP' | 'EU' | 'CN' | 'AU' | 'CA';

export interface GraphNode {
  id:         string;
  label:      string;
  sector:     string;
  portfolio:  string;
  color:      string;
  // ── Intelligence fields ──────────────────────────────────────
  importance: number;      // 1–10  (drives node radius)
  role:       NodeRole;
  risk:       RiskLevel;   // supply chain risk / concentration risk
  monopoly:   boolean;     // true = no real substitute exists
  region:     Region;
  note?:      string;      // short analyst note shown in panel
  alternatives?: Array<{ id: string; label: string; feasibility: 'short-term' | 'long-term' | 'partial' | 'blocked' | 'immediate' }>;
}

export type ContractType = 'sole-source' | 'captive' | 'long-term' | 'strategic' | 'spot' | 'multi-source';

export interface GraphEdge {
  source:        string;
  target:        string;
  type:          EdgeType;
  label:         string;
  // ── Intelligence fields ──────────────────────────────────────
  strength:      number;       // 1–10  (drives edge thickness)
  concentration: number;       // 0–1   (% of buyer's supply from this single source)
  substitutable: boolean;      // can the buyer replace this supplier?
  risk:          RiskLevel;
  contractType?: ContractType; // nature of the commercial relationship
  value?:        string;       // e.g. "$4B contract", "~30% COGS"
}

